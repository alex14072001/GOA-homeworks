#შეამოწმე, არის თუ არა ორი რიცხვი ერთმანეთზე მეტი და 10-ის ჯერადი:

num1 = int(input("შეიტანეთ პირველი რიცხვი: "))
num2 = int(input("შეიტანეთ მეორე რიცხვი: "))

if num1 > num2 and num1 % 10 == 0 and num2 % 10 == 0:
    print("ორივე რიცხვი არის 10-ის ჯერადი და პირველი რიცხვი მეტია მეორეს.")
else:
    print("დავალება ვერ შესრულდა.")
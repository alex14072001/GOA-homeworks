#მომხაარებელ შემოატანინეთ სახელი და გვარი ეს სახელი და გვარი გააერთიანეთ და შეაბრუნეთ

first_name = input("შეიყვანეთ სახელი: ")
last_name = input("შეიყვანეთ გვარი: ")

# სახელისა და გვარის გაერთიანება და შებრუნება
reversed_name = (first_name + " " + last_name)[::-1]

# შედეგის ჩვენება
print("შებრუნებული სახელი და გვარი:", reversed_name)
